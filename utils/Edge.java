class Edge {
    private int nodeIndex;
    private int weight;

    // Getters
    public getNodeIndex() {
        return nodeIndex;
    }

    public getWeight() {
        return weight;
    }

    // Setters
    public setNodeIndex(int nodeIndex) {
        this.nodeIndex = nodeIndex;
    }

    public setWeight(int weight) {
        this.weight = weight;
    }
}
